	private String wingType;//sweptback, Delta, Straight,tapered
	private String wheelType;//fixed or retractable
	
	public AirplaneCat(String Air, String Cla,String Fuel,String wing, String wheel) {
		super(Air,Cla,Fuel);
		setWingType(wing);
		setWheelType(wheel);
	}

	/**
	 * @return the wingType
	 */
	public String getWingType() {
		return wingType;
	}

	/**
	 * @return the wheelType
	 */
	public String getWheelType() {
		return wheelType;
	}

	/**
	 * @param wingType the wingType to set
	 */
	public void setWingType(String wingType) {
		this.wingType = wingType;
	}

	/**
	 * @param wheelType the wheelType to set
	 */
	public void setWheelType(String wheelType) {
		this.wheelType = wheelType;
	}
	@Override
	public String toString()
	{
		return String.format("%s%s%s\n%s%s\n%s$%f",super.toString(),"Wing Type: ", getWingType(),"Wheel Type: ", getWheelType(),"Cost Per Hour: ",costPerHOur());
	}

	/* (non-Javadoc)
	 * @see chapter10.Aircraft#costPerHOur()
	 */
